# Parte 2 - Simulador de Evacuacao

# Mapeei o 1 andar da UNICID.

# O agente tenta sair da sala do fundo ate a calcada da Rua Cesario Galeno.

# Lista 1: os lugares do trajeto que identifiquei

locais = [

  "Sala de aula - fundo do corredor (1 andar)",

  "Corredor principal do 1 andar",

  "Copa com cadeiras empilhadas",

  "Porta corta-fogo",

  "Base da escada de saida",

  "Corredor do terreo",

  "Porta de saida lateral",

  "Calcada - Rua Cesario Galeno (SAIDA)"

]


# Lista 2: o que tem em cada lugar (mesma ordem da lista acima)

situacoes = [

  "livre",

  "extintor na parede",

  "cadeiras bloqueando passagem",

  "trava magnetica - precisa de cracha",

  "cracha de emergencia na caixinha",

  "fumaca leve no corredor",

  "barra antipânico",

  "saida"

]


# Inventario e estado do agente

inventario  = []

energia   = 18

posicao   = 0 # começa na posição 0 (sala de aula)

print("Inicio: " + locais[posicao])

print("Energia: " + str(energia))

print("==========================")


# Loop while: continua enquanto nao chegou na saida e ainda tem energia

while posicao < len(locais) - 1 and energia > 0:

  lugar  = locais[posicao]

  situacao = situacoes[posicao]

  print("\nAgente em: " + lugar)

  print("Situacao: " + situacao)

  print("Energia: " + str(energia))

  print("Inventario: " + str(inventario))


  # Avalia o que tem naquele lugar e decide o que fazer

  if situacao == "livre":

    print("-> Caminho livre. Seguindo em frente.")

    posicao = posicao + 1

    energia = energia - 1


  elif situacao == "extintor na parede":

    print("-> Peguei o extintor. Pode ser util la na frente.")

    inventario.append("extintor")

    posicao = posicao + 1

    energia = energia - 1


  elif situacao == "cadeiras bloqueando passagem":

    if energia >= 4:

      print("-> Tinha cadeiras no caminho. Empurrei e passei.")

      posicao = posicao + 1

      energia = energia - 3

    else:

      print("-> Pouca energia, mas tentei passar pela lateral. Deu.")

      posicao = posicao + 1

      energia = energia - 1



  elif situacao == "trava magnetica - precisa de cracha":

    if "cracha" in inventario:

      print("-> Passei o cracha na trava. Porta abriu.")

      inventario.remove("cracha")

      posicao = posicao + 1

      energia = energia - 1

    else:

      # Nao tem cracha — verifica se tem extintor pra forcar

      if "extintor" in inventario:

        print("-> Sem cracha! Usei o extintor pra forcar a trava. Abriu.")

        inventario.remove("extintor")

        posicao = posicao + 1

        energia = energia - 4

      else:

        print("-> Sem cracha e sem extintor. Preciso voltar buscar.")

        posicao = posicao - 1

        energia = energia - 4


  elif situacao == "cracha de emergencia na caixinha":

    print("-> Achei o cracha de emergencia na caixinha da parede!")

    inventario.append("cracha")

    posicao = posicao + 1

    energia = energia - 1


  elif situacao == "fumaca leve no corredor":

    if "extintor" in inventario:

      print("-> Tinha fumaca. Usei o extintor pra abrir caminho.")

      inventario.remove("extintor")

      posicao = posicao + 1

      energia = energia - 1

    else:

      print("-> Fumaca sem extintor. Agachei e passei devagar.")

      posicao = posicao + 1

      energia = energia - 3



  elif situacao == "barra antipânico":

    if energia >= 3:

      print("-> Empurrei a barra antipanico com o ombro. Abriu.")

      posicao = posicao + 1

      energia = energia - 3

    else:

      print("-> Quase sem energia, mas bati na barra. Abriu!")

      posicao = posicao + 1

      energia = 0


# Verifica se conseguiu ou nao sair

print("\n==========================")

if posicao >= len(locais) - 1:

  print("CONSEGUIU SAIR!")

  print("Chegou em: " + locais[posicao])

  print("Energia que sobrou: " + str(energia))

else:

  print("NAO CONSEGUIU SAIR.")

  print("Ficou sem energia em: " + locais[posicao])