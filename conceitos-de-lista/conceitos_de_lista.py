# Coletei dados de 4 pontos do meu trajeto diário em São Paulo

# Fonte: CETESB e Climatempo, medidos de manhã (~8h) e de tarde (~15h)

# Cada item da lista tem: [nome do local, horario, temperatura, umidade, IQA]

dados_locais = [

  ["Parque Ibirapuera",   "Manha", 21, 75, 35],

  ["Parque Ibirapuera",   "Tarde", 26, 60, 42],

  ["Av. 23 de Maio",    "Manha", 24, 66, 88],

  ["Av. 23 de Maio",    "Tarde", 32, 45, 115],

  ["Ponto Radial Leste",  "Manha", 23, 69, 63],

  ["Ponto Radial Leste",  "Tarde", 31, 50, 80],

  ["Trabalho - Barra Funda", "Manha", 25, 62, 95],

  ["Trabalho - Barra Funda", "Tarde", 33, 43, 128],

]


# Loop principal: passa por cada local da lista

for local in dados_locais:


  # Pego cada informação pelo índice da lista

  nome    = local[0]

  horario  = local[1]

  temperatura = local[2]

  umidade  = local[3]

  iqa    = local[4]

  print("--------------------")

  print("Local:   " + nome)

  print("Horario:  " + horario)

  print("Temperatura: " + str(temperatura) + " graus")

  print("Umidade:  " + str(umidade) + "%")

  print("IQA:    " + str(iqa))


  # Classifica a qualidade do ar pelas faixas oficiais da CETESB

  match True:

    case _ if iqa <= 40:

      qualidade = "Boa"

    case _ if iqa <= 80:

      qualidade = "Moderada"

    case _ if iqa <= 120:

      qualidade = "Ruim"

    case _ if iqa <= 200:

      qualidade = "Muito Ruim"

    case _:

      qualidade = "Pessima"

  print("Qualidade do ar: " + qualidade)


  # Calculo a nota de conforto (0 a 10)

  # Temperatura ideal pra mim: entre 20 e 26 graus

  if 20 <= temperatura <= 26:

    nota_temp = 10

  elif temperatura < 20:

    nota_temp = 10 - (20 - temperatura) * 1.5

  else:

    nota_temp = 10 - (temperatura - 26) * 1.2


  # Garante que a nota nao passa de 0 pra baixo

  if nota_temp < 0:

    nota_temp = 0


  # Umidade ideal: entre 50% e 70%

  if 50 <= umidade <= 70:

    nota_umidade = 10

  elif umidade < 50:

    nota_umidade = 10 - (50 - umidade) * 0.4

  else:

    nota_umidade = 10 - (umidade - 70) * 0.3

  if nota_umidade < 0:

    nota_umidade = 0



  # IQA: quanto menor o IQA, melhor. Converto em nota invertida

  nota_iqa = 10 - (iqa / 200) * 10

  if nota_iqa < 0:

    nota_iqa = 0



  # Nota final com pesos: temperatura 40%, umidade 30%, IQA 30%

  nota_final = (nota_temp * 0.4) + (nota_umidade * 0.3) + (nota_iqa * 0.3)

  nota_final = round(nota_final, 1)

  print("Nota de conforto: " + str(nota_final) + "/10")


  # Dou um veredicto baseado na nota
  if nota_final >= 7:

    print("Situacao: bom pra caminhar, sem problema nenhum.")

  elif nota_final >= 4:

    print("Situacao: da pra aguentar, mas quem tem asma ou rinite sofre.")

  else:

    print("Situacao: ruim demais, prefiro esperar dentro de algum lugar.")

print("--------------------")

