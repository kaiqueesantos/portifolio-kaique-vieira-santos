def processar_vendas():

  total = 0.0

  itens = 0

  quantidade = int(input("Quantos produtos diferentes você comprou? "))

  for i in range(quantidade):

    print("\nProduto", i + 1)

    nome = input("Nome do produto: ")

    preco = float(input("Preço unitário: R$ "))

    qtd = int(input("Quantidade: "))

    if preco <= 0 or qtd <= 0:

      print("Valores inválidos para", nome, "- pulando...")

    else:

      subtotal = preco * qtd

      total = total + subtotal

      itens = itens + qtd

      print("Subtotal:", subtotal)

  if total > 500:

    total_com_desconto = total * 0.90

    print("\nDesconto de 10% aplicado!")

  elif total > 200:

    total_com_desconto = total * 0.95

    print("\nDesconto de 5% aplicado!")

  else:

    total_com_desconto = total

    print("\nSem desconto.")

  print("Total de itens:", itens)

  print("Total a pagar: R$", round(total_com_desconto, 2))

def analisar_clima():

  soma = 0.0

  dias_quentes = 0

  alerta = False

  for dia in range(1, 8):

    temp = float(input("Temperatura do dia " + str(dia) + ": "))

    soma = soma + temp

    if temp > 35:

      dias_quentes = dias_quentes + 1

    if temp > 45 or temp < -5:

      alerta = True

  media = soma / 7

  print("\nMédia da semana:", round(media, 1), "°C")

  print("Dias acima de 35°C:", dias_quentes)

  if alerta:

    print("ATENÇÃO: temperatura perigosa detectada!")

  else:

    print("Clima normal na semana.")

