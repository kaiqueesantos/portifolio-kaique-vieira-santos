def simulador_poupanca():

  saldo = float(input("Valor inicial: R$ "))

  taxa = float(input("Taxa de juros mensal (%): "))

  meses = int(input("Quantos meses quer simular? "))

  for m in range(1, meses + 1):

    aporte = float(input("\nMês " + str(m) + " - Quanto vai depositar? R$ "))

    saldo = saldo + aporte

    juros = saldo * (taxa / 100)

    saldo = saldo + juros

    print("Saldo no mês", m, ": R$", round(saldo, 2))

    if saldo >= 10000:

      print("Parabéns! Você passou de R$10.000 no mês", m)

  print("\nSaldo final após", meses, "meses: R$", round(saldo, 2))